package operatoret_aritmetik;

public class StringExample {
    public static void main(String[] args) {
        String name = "Naim";
        String surname = "Sulejmani";
        String fullName = name + " " + surname; // Naim Sulejmani
        System.out.println(fullName);
        int x = 3;
        int y = 5;
        String point = "(" + x + ", " + y + ")";
        System.out.println(point);

        String fituesi = "Real Madrid";
        System.out.println(fituesi.charAt(0)); // cilen shkronje po i qasemi
        System.out.println(fituesi.length()); // gjatesine e tekstit
        System.out.println(fituesi.charAt(fituesi.length() - 1));

    }
}







