package selection_decision;

public class SymbolComparasion {
    public static void main(String[] args) {
        char shkronja = 'H';

        if (shkronja >= 'A' && shkronja <= 'Z') {
            System.out.println("PO");
        } else {
            System.out.println("JO");
        }

        if (shkronja > 50) {
            System.out.println("PO");
        } else {
            System.out.println("JO");
        }
    }
}















