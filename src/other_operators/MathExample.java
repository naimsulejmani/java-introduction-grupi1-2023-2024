package other_operators;

public class    MathExample {
    public static void main(String[] args) {

        System.out.println(Math.abs(-100)); // vlera absolute
        System.out.println(Math.max(23.5, 2)); // metoda qe na kthen maximumin e dy numrave
        System.out.println(Math.min(23.5, 2)); // metoda qe na kthen minimumin e dy numrave
        System.out.println(Math.pow(2, 10)); // 2^10 -> FUQIA - POWER
        System.out.println(Math.sqrt(4)); // RRENJA KATRORE - SQUARE ROOT
        System.out.println(Math.E);
        System.out.println(Math.PI);
        System.out.println(Math.toRadians(90));
        System.out.println(Math.toRadians(180));
        System.out.println(Math.toDegrees(1.75));
        System.out.println(Math.toDegrees(0.5));

        System.out.println(Math.sin(Math.toRadians(90)));
        System.out.println(Math.sin(Math.toRadians(180)));

        System.out.println(Math.cos(Math.toRadians(90)));
        System.out.println(Math.cos(Math.toRadians(180)));

        System.out.println(Math.tan(Math.toRadians(90)));
        System.out.println(Math.tan(Math.toRadians(180)));
        System.out.println(Math.random());
        System.out.println(Math.round(2.45));
        System.out.println(Math.round(2.51));
        System.out.println(Math.ceil(2.71));
        System.out.println(Math.floor(2.71));
        System.out.println(Math.log10(100));
        System.out.println(Math.log(4));

    }
}














