package loops_iterations_repetitions.ushtrimet;

public class Alfabeti {
    public static void main(String[] args) {
        char shkronja = 'A';

        while (shkronja <= 'Z') {
            System.out.println(shkronja++);

        }

        for (char i = 'a'; i <= 'z'; i++) {
            System.out.println(i);
        }
    }
}
