package arrays;

public class ArrayExample2 {
    public static void main(String[] args) {
        int[] array = new int[10];
        int x = array[0];
        System.out.println(x);
        x = 10;
        System.out.println(x);
        System.out.println(array[0]);
        array[0] = 5;
        System.out.println(x);
        System.out.println(array[0]);
    }
}











